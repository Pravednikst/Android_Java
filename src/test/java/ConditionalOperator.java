public class ConditionalOperator {
    public static void main(String[] args) {
        int temp = 19;
        if(temp > 25){
            System.out.println("Кондикионер включен");
        } else if (temp < 18){
            System.out.println("Кондиционер выключен");
        } else {
            System.out.println("Кондиционер ни чего не делает");
        }


    }

}
